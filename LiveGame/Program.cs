    public static int NeighbourCount(int x, int y, char[,] map)
    {
        int count = 0;

        if (x - 1 > 0 & y - 1 > 0)
        {
            if (IsAlive(map[x - 1, y - 1]))
                count++;
        }
        if (x - 1 > 0)
        {
            if (IsAlive(map[x - 1, y]))
                count++;
        }
        if (x - 1 > 0 & y + 1 < map.GetLength(1))
        {
            if (IsAlive(map[x - 1, y + 1]))
                count++;
        }


        if (y - 1 > 0)
        {
            if (IsAlive(map[x, y - 1]))
                count++;
        }
        if (y + 1 < map.GetLength(1))
        {
            if (IsAlive(map[x, y + 1]))
                count++;
        }

        if (x + 1 < map.GetLength(0) & y - 1 > 0)
        {
            if (IsAlive(map[x + 1, y - 1]))
                count++;
        }
        if (x + 1 < map.GetLength(0))
        {
            if (IsAlive(map[x + 1, y]))
                count++;
        }
        if (x + 1 < map.GetLength(0) & y + 1 < map.GetLength(1))
        {
            if (IsAlive(map[x + 1, y + 1]))
                count++;
        }

        return count;
    }

    public static char StateOfCell(int neighbour, bool isAlive, char currChar)
    {
        if (isAlive)
        {
            if (neighbour > 3 || neighbour < 2) return ' ';
        }
        else if (neighbour == 3) return '0';

        return currChar;
    }

    public static void PrintMap(char[,] map)
